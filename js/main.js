// ===== Live fill-light demo (hero signature) =====
function kelvinToRGB(kelvin) {
  const t = kelvin / 100;
  let r, g, b;
  if (t <= 66) { r = 255; } else { r = 329.698727446 * Math.pow(t - 60, -0.1332047592); }
  if (t <= 66) { g = 99.4708025861 * Math.log(t) - 161.1195681661; }
  else { g = 288.1221695283 * Math.pow(t - 60, -0.0755148492); }
  if (t >= 66) { b = 255; } else if (t <= 19) { b = 0; }
  else { b = 138.5177312231 * Math.log(t - 10) - 305.0447927307; }
  const c = v => Math.round(Math.max(0, Math.min(255, v)));
  return [c(r), c(g), c(b)];
}

let currentKelvin = 4500;
let currentScene = 'sceneNeutral';

function updateLightScene() {
  const slider = document.getElementById('ldSlider');
  const screen = document.getElementById('ldScreen');
  const glow = document.getElementById('ldGlow');
  const kEl = document.getElementById('ldKelvin');
  const sEl = document.getElementById('ldSceneLabel');
  if (!screen || !glow) return;
  const [r, g, b] = kelvinToRGB(currentKelvin);
  const rgb = `rgb(${r}, ${g}, ${b})`;
  const dim = `rgb(${Math.round(r * 0.34)}, ${Math.round(g * 0.34)}, ${Math.round(b * 0.34)})`;
  screen.style.background = `radial-gradient(120% 90% at 50% 45%, ${rgb} 0%, ${dim} 100%)`;
  glow.style.background = rgb;
  glow.style.boxShadow = `0 0 90px 42px rgba(${r}, ${g}, ${b}, 0.45)`;
  if (kEl) kEl.textContent = currentKelvin + 'K';
  if (sEl) { const t = I18N[currentLang]; sEl.textContent = (t && t[currentScene]) || currentScene; }
  if (slider && Number(slider.value) !== currentKelvin) slider.value = currentKelvin;
}

function initLightDemo() {
  const slider = document.getElementById('ldSlider');
  if (!slider) return;
  slider.addEventListener('input', () => {
    currentKelvin = Number(slider.value);
    currentScene = 'sceneNeutral';
    document.querySelectorAll('.ld-chip').forEach(c => c.classList.remove('active'));
    updateLightScene();
  });
  document.querySelectorAll('.ld-chip').forEach(chip => {
    chip.addEventListener('click', () => {
      document.querySelectorAll('.ld-chip').forEach(c => c.classList.remove('active'));
      chip.classList.add('active');
      currentKelvin = Number(chip.getAttribute('data-kelvin'));
      currentScene = chip.getAttribute('data-scene') || 'sceneNeutral';
      updateLightScene();
    });
  });
  updateLightScene();
}

// ===== Dual-color lab =====
function initDuoDemo() {
  const top = document.getElementById('duoTop');
  const bottom = document.getElementById('duoBottom');
  const seam = document.getElementById('duoSeam');
  const stage = document.getElementById('duoStage');
  if (!top || !bottom) return;
  const state = { top: '#FFB25E', bottom: '#7FC4FF' };

  function paint() {
    top.style.background = state.top;
    bottom.style.background = state.bottom;
    if (seam) seam.style.background = 'linear-gradient(to bottom, ' + state.top + ', ' + state.bottom + ')';
    if (stage) stage.style.boxShadow = 'var(--shadow-lg), 0 -18px 70px -14px ' + state.top + '66, 0 18px 70px -14px ' + state.bottom + '66';
    document.querySelectorAll('.duo-swatch').forEach(sw => {
      sw.classList.toggle('active', state[sw.getAttribute('data-half')] === sw.getAttribute('data-color'));
    });
    document.querySelectorAll('.duo-combo').forEach(cb => {
      cb.classList.toggle('active', cb.getAttribute('data-top') === state.top && cb.getAttribute('data-bottom') === state.bottom);
    });
  }
  document.querySelectorAll('.duo-swatch').forEach(sw => {
    sw.addEventListener('click', () => {
      state[sw.getAttribute('data-half')] = sw.getAttribute('data-color');
      paint();
    });
  });
  document.querySelectorAll('.duo-combo').forEach(cb => {
    cb.addEventListener('click', () => {
      state.top = cb.getAttribute('data-top');
      state.bottom = cb.getAttribute('data-bottom');
      paint();
    });
  });
  paint();
}

// ===== Lighting playbook scroller =====
function initTips() {
  const track = document.getElementById('tipsTrack');
  if (!track) return;
  const step = () => (track.children[1] ? track.children[1].offsetLeft - track.children[0].offsetLeft : 320);
  const prev = document.getElementById('tipsPrev');
  const next = document.getElementById('tipsNext');
  if (prev) prev.addEventListener('click', () => track.scrollBy({ left: -step(), behavior: 'smooth' }));
  if (next) next.addEventListener('click', () => track.scrollBy({ left: step(), behavior: 'smooth' }));
}

